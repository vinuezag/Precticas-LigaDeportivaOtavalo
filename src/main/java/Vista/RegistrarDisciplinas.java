    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String deporteSeleccionado = (String) listDeportes.getSelectedItem();
    
    if (!deporteSeleccionado.equals("Elegir una opci贸n")) {
        ConsultasDisciplinas consultasDisciplinas = new ConsultasDisciplinas();
        
        if (consultasDisciplinas.eliminar(deporteSeleccionado)) {
            System.out.println("Disciplina eliminada correctamente.");
        } else {
            System.out.println("Error al eliminar la disciplina.");
        }
    } else {
        System.out.println("Por favor seleccione un deporte.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Disciplinas disciplina = new Disciplinas();
    disciplina.setDeporte(txtDeporte.getText()); 
    
    ConsultasDisciplinas consultaDisciplinas = new ConsultasDisciplinas();
    
    if (consultaDisciplinas.registrar(disciplina)) {
        System.out.println("Disciplina agregada correctamente.");
    } else {
        System.out.println("Error al agregar la disciplina.");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed


