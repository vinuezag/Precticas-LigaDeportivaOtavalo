    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        ConsultasDeportista consultasDeportista = new ConsultasDeportista();
        Deportistas estadisticas = consultasDeportista.consultarEstadisticasDeportistas();

        // Crear dataset para el primer gráfico de pastel (hombres vs mujeres)
        DefaultPieDataset dataset1 = new DefaultPieDataset();
        dataset1.setValue("Hombres", estadisticas.getNumHombresDeportistas());
        dataset1.setValue("Mujeres", estadisticas.getNumMujeresDeportistas());

        // Crear el gráfico de pastel para hombres vs mujeres
        JFreeChart chart1 = ChartFactory.createPieChart(
                "Hombres vs Mujeres",
                dataset1,
                true,
                true,
                false
        );

        // Crear dataset para el segundo gráfico de pastel (deporte más vs menos representado)
        DefaultPieDataset dataset2 = new DefaultPieDataset();
        dataset2.setValue("Más Representado", 1); // Supongamos que ya tienes el deporte más representado
        dataset2.setValue("Menos Representado", 1); // Supongamos que ya tienes el deporte menos representado

        // Crear el gráfico de pastel para deporte más vs menos representado
        JFreeChart chart2 = ChartFactory.createPieChart(
                "Categoria Más Representado vs Menos Representado",
                dataset2,
                true,
                true,
                false
        );

        // Crear el ChartPanel para el primer gráfico y establecer su tamaño
ChartPanel chartPanel1 = new ChartPanel(chart1);
chartPanel1.setPreferredSize(new java.awt.Dimension(200, 150));

// Crear el ChartPanel para el segundo gráfico y establecer su tamaño
ChartPanel chartPanel2 = new ChartPanel(chart2);
chartPanel2.setPreferredSize(new java.awt.Dimension(200, 150));

// Limpiar los paneles antes de agregar los nuevos gráficos
pblGenero.removeAll();
pnlMasoMenos.removeAll();

// Agregar los gráficos a los paneles correspondientes en la interfaz de usuario
pblGenero.setLayout(new BorderLayout());
pblGenero.add(chartPanel1, BorderLayout.CENTER);

pnlMasoMenos.setLayout(new BorderLayout());
pnlMasoMenos.add(chartPanel2, BorderLayout.CENTER);

// Refrescar la interfaz de usuario para que los cambios sean visibles
pblGenero.revalidate();
pnlMasoMenos.revalidate();
pblGenero.repaint();
pnlMasoMenos.repaint();

    }//GEN-LAST:event_btnGraficarActionPerformed


