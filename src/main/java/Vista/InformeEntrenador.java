    private void cargarEstadisticaGeneral() {
    // Obtener las estadísticas de deportistas
    ConsultasDeportista consulta = new ConsultasDeportista();
    Deportistas deportista = consulta.consultarEstadisticasDeportistas();

    // Establecer los valores en los JLabel
    lblPromEdad.setText("Promedio de Edad de Deportistas: " + deportista.getPromedioEdad());
    lblMasculinos.setText("Número de Deportistas Masculinos: " + deportista.getNumHombresDeportistas());
    lblFemeninos.setText("Número de Deportistas Femeninos: " + deportista.getNumMujeresDeportistas());
    lblDeporteMas.setText("Deporte con más Deportistas: " + deportista.getDeporteMasRegistros());
    lblDeporteMenos.setText("Deporte con menos Deportistas: " + deportista.getDeporteMenosRegistros());

    // Diagrama de pastel para número de deportistas por género
    DefaultPieDataset datasetGenero = new DefaultPieDataset();
    datasetGenero.setValue("Masculino", deportista.getNumHombresDeportistas());
    datasetGenero.setValue("Femenino", deportista.getNumMujeresDeportistas());
    JFreeChart chartGenero = ChartFactory.createPieChart("Distribución por Género", datasetGenero);
    ChartPanel chartPanelGenero = new ChartPanel(chartGenero);
    chartPanelGenero.setPreferredSize(new java.awt.Dimension(200, 150)); // Establecer el tamaño del gráfico
    pblGenero.removeAll();
    pblGenero.add(chartPanelGenero);
    pblGenero.revalidate();
    pblGenero.repaint();

    // Diagrama de pastel para deporte con más y menos registros
    DefaultPieDataset datasetDeporte = new DefaultPieDataset();
    datasetDeporte.setValue("Más Registros", 1); // Puedes establecer el valor según tu lógica
    datasetDeporte.setValue("Menos Registros", 1); // Puedes establecer el valor según tu lógica
    JFreeChart chartDeporte = ChartFactory.createPieChart("Deporte con más y menos Registros", datasetDeporte);
    ChartPanel chartPanelDeporte = new ChartPanel(chartDeporte);
    chartPanelDeporte.setPreferredSize(new java.awt.Dimension(200, 150)); // Establecer el tamaño del gráfico
    pnlMasoMenos.removeAll();
    pnlMasoMenos.add(chartPanelDeporte);
    pnlMasoMenos.revalidate();
    pnlMasoMenos.repaint();
}


    
    private void cargarDisciplinas() {
        ConsultasDisciplinas consultasDisciplinas = new ConsultasDisciplinas();
        List<String> disciplinas = consultasDisciplinas.consultarDisciplinas();
        
        listDeportes.removeAllItems();
        
        listDeportes.addItem("Elegir una opción");
        
        for (String disciplina : disciplinas) {
            listDeportes.addItem(disciplina);
        }
    }

